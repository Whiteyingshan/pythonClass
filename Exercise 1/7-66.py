n = eval(input())
if n < 0:
    print(0)
elif n <= 100000:
    print(n * 0.1)
elif 100000 < n <= 200000:
    print((n - 100000) * 0.075 + 100000 * 0.1)
elif 200000 < n <= 400000:
    print((n - 200000) * 0.05 + 100000 * 0.075 + 100000 * 0.1)
elif 400000 < n <= 600000:
    print((n - 400000) * 0.03 + 200000 * 0.05 + 100000 * 0.075 + 100000 * 0.1)
elif 600000 < n <= 1000000:
    print((n - 600000) * 0.015 + 200000 * 0.03 + 200000 * 0.05 + 100000 * 0.075 + 100000 * 0.1)
else:
    print((n - 1000000) * 0.01 + 400000 * 0.015 + 200000 * 0.03 + 200000 * 0.05 + 100000 * 0.075 + 100000 * 0.1)
